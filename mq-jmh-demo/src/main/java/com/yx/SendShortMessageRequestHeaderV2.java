package com.yx;

/**
 * Use short variable name to speed up FastJson deserialization process.
 */
public class SendShortMessageRequestHeaderV2{
    
    private String a; // producerGroup;
    
    private String b; // topic;
    
    private String c; // defaultTopic;
    
    private String d; // defaultTopicQueueNums;


    public SendShortMessageRequestHeaderV2() {
    }

    public SendShortMessageRequestHeaderV2(String a, String b, String c, String d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }
}